//PAGINATION FUNCTION

function pagination(data,page,imagePerPage){
  let trimstart = (page - 1) * imagePerPage;
  let trimend = trimstart + imagePerPage;
  let paginatedData = data.slice(trimstart,trimend);
  let pages = Math.ceil(data.length/imagePerPage);
  return {
    'data': paginatedData,
    'pages': pages
  }
}

