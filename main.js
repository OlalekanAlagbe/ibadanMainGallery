function generateButton(data,imagePerPage){
  let paginatedBtn = [];
  let btnNo = Math.ceil(data.length / imagePerPage);
  for(let i = 0; i < btnNo; i++){
    paginatedBtn.push(`<li class="link" onclick="directPage(this.innerText)">${i+1}</li>`);
  }
  paginatedBtn = paginatedBtn.join("");
  paginateContainer.innerHTML =paginatedBtn;
  paginateContainer.firstElementChild.classList.add('active');

}

